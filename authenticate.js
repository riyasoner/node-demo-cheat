import jwt from "jsonwebtoken" ;
export const verify = (req, res, next) => {
  const authHeader = req.headers.authorization;
  if (!authHeader) {
    res.status(401).json({
        status: 'fail',
        message: 'Unauthorized!no token',
      });
  }
  try {
    const user = jwt.verify(authHeader, 'secretkey');
    req.user = user;
    next();
  } catch (error) {
    res.status(401).json({
        status: 'fail',
        message: 'Unauthorized!',
      });
  }
};
